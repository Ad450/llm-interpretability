def visualize_topk_distribution(fp32_results, int8_results, model_name, k=5):
    """
    Visualize the distribution of top-K overlap scores across all prompts.
    Args:
        fp32_results: List of dicts from extract_attention_weights (FP32)
        int8_results: List of dicts from extract_attention_weights (INT8)
        model_name: Name of the model for plot title
        k: Number of top tokens to consider
    """
    metrics = AttentionMetrics()

    # Collect per-prompt scores
    per_prompt_scores = []
    for i, (fp32_result, int8_result) in enumerate(zip(fp32_results, int8_results)):
        prompt_layer_scores = []

        for fp32_layer, int8_layer in zip(fp32_result['attentions'], int8_result['attentions']):
            # slice batch dimension out
            fp32_single = fp32_layer[0]
            int8_single = int8_layer[0]
            score = metrics.top_k_overlap_single_prompt(fp32_single, int8_single, k=k)
            prompt_layer_scores.append(score)

        # Average across layers for this prompt
        per_prompt_scores.append(np.mean(prompt_layer_scores))

    per_prompt_scores = np.array(per_prompt_scores)

    # Plot
    fig, axes = plt.subplots(1, 3, figsize=(16, 5))
    fig.suptitle(f"{model_name} — Top-{k} Overlap Distribution across {len(per_prompt_scores)} prompts", fontsize=14)

    # Histogram
    axes[0].hist(per_prompt_scores, bins=20, edgecolor='black', color='steelblue')
    axes[0].axvline(per_prompt_scores.mean(), color='red', linestyle='--', label=f'Mean: {per_prompt_scores.mean():.3f}')
    axes[0].set_xlabel('Top-K Overlap Score')
    axes[0].set_ylabel('Number of prompts')
    axes[0].set_title('Histogram')
    axes[0].legend()

    # KDE (smooth density curve)
    sns.kdeplot(per_prompt_scores, ax=axes[1], fill=True, color='steelblue')
    axes[1].axvline(per_prompt_scores.mean(), color='red', linestyle='--', label=f'Mean: {per_prompt_scores.mean():.3f}')
    axes[1].set_xlabel('Top-K Overlap Score')
    axes[1].set_ylabel('Density')
    axes[1].set_title('Density curve')
    axes[1].legend()

    # Boxplot
    axes[2].boxplot(per_prompt_scores, vert=True)
    axes[2].set_ylabel('Top-K Overlap Score')
    axes[2].set_title('Boxplot')
    axes[2].set_xticks([])

    plt.tight_layout()
    plt.show()

    # Print summary stats
    print(f"\nSummary for {model_name} Top-{k} Overlap:")
    print(f"  Mean:   {per_prompt_scores.mean():.4f}")
    print(f"  Std:    {per_prompt_scores.std():.4f}")
    print(f"  Min:    {per_prompt_scores.min():.4f}")
    print(f"  Max:    {per_prompt_scores.max():.4f}")
    print(f"  Median: {np.median(per_prompt_scores):.4f}")



