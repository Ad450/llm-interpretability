def extract_all_attention_weights(model_fp32, model_int8, tokenizer,
                                  prompts, model_name, device='cuda', batch_size=10):
    """
    Extract attention weights from both FP32 and INT8 models for all prompts.
    Returns:
        dict with results for both precisions
    """
    print(f"\n{'='*70}")
    print(f"Extracting attention weights for {model_name}")
    print(f"{'='*70}")

    results_fp32 = []
    results_int8 = []

    # Determine device for each model
    device_fp32 = 'cpu'  # FP32 on CPU to save GPU memory
    device_int8 = device  # INT8 on GPU

    # ↓ iterate in batches instead of one prompt at a time
    for i in tqdm(range(0, len(prompts), batch_size), desc=f"{model_name} extraction"):
        batch = prompts[i:i + batch_size]  # ← slice the batch here

        # Extract FP32
        batch_results_fp32 = extract_attention_weights(
            model_fp32, tokenizer, batch, device=device_fp32
        )
        for j, result in enumerate(batch_results_fp32):
            result['prompt_id'] = i + j
            result['precision'] = 'fp32'
        results_fp32.extend(batch_results_fp32)  # ← extend, not append

        # Extract INT8
        batch_results_int8 = extract_attention_weights(
            model_int8, tokenizer, batch, device=device_int8
        )
        for j, result in enumerate(batch_results_int8):
            result['prompt_id'] = i + j
            result['precision'] = 'int8'
        results_int8.extend(batch_results_int8)  # ← extend, not append

    print(f"✅ Extracted attention from {len(prompts)} prompts")
    print(f"   - FP32: {len(results_fp32)} samples")
    print(f"   - INT8: {len(results_int8)} samples")

    return {
        'fp32': results_fp32,
        'int8': results_int8,
        'model_name': model_name,
        'num_prompts': len(prompts),
        'timestamp': datetime.now().isoformat()
    }



