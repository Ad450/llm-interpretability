def extract_attention_weights(model, tokenizer, prompts, device='cuda'):
    """
    Extract attention weights from a model for a given batch of prompts.
    Args:
        model: The transformer model
        tokenizer: Corresponding tokenizer
        prompts: List of input text strings (or a single string)
        device: 'cuda' or 'cpu'
    Returns:
        List of dicts with attention weights, tokens, and metadata
    """
    # Handle single string input by wrapping in a list
    if isinstance(prompts, str):
        prompts = [prompts]

    # Ensure padding token exists
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
        tokenizer.pad_token_id = tokenizer.eos_token_id

    # Tokenize the whole batch at once
    inputs = tokenizer(
        prompts,
        return_tensors="pt",
        padding=True,
        truncation=True,
        max_length=512
    )

    # Move to device
    inputs = {k: v.to(device) for k, v in inputs.items()}

    # Get attention weights
    with torch.no_grad():
        outputs = model(**inputs, output_attentions=True)

    # Extract attention (tuple of tensors, one per layer)
    attentions = outputs.attentions  # Tuple of (batch, heads, seq_len, seq_len)

    # Convert to numpy and move to CPU
    attentions_numpy = [
        attn.cpu().numpy() for attn in attentions
    ]

    # ↓ Build a result dict per prompt in the batch
    results = []
    for idx, prompt in enumerate(prompts):
        tokens = tokenizer.convert_ids_to_tokens(inputs['input_ids'][idx])
        results.append({
            'attentions': [attn[idx] for attn in attentions_numpy],  # per-sample slice
            'tokens': tokens,
            'num_layers': len(attentions_numpy),
            'num_heads': attentions_numpy[0].shape[1],
            'seq_length': attentions_numpy[0].shape[2],
            'prompt': prompt
        })

    return results  # ← always returns a list now

